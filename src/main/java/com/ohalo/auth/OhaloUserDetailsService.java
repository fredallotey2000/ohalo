package com.ohalo.auth;

import com.ohalo.data.User;
import com.ohalo.data.UserRepository;
import org.springframework.security.core.userdetails.UserDetails;
import org.springframework.security.core.userdetails.UserDetailsService;
import org.springframework.security.core.userdetails.UsernameNotFoundException;
import org.springframework.stereotype.Service;

/**
 *
 * @author Bsystems4
 */
@Service
public class OhaloUserDetailsService implements UserDetailsService {

    private final UserRepository userRepository;

    public OhaloUserDetailsService(UserRepository userRepository) {
        this.userRepository = userRepository;
    }

    @Override
    public UserDetails loadUserByUsername(String username) throws UsernameNotFoundException {
        User user = this.userRepository.findByUsername(username);
        if(user == null){
            throw new UsernameNotFoundException("Cannot find user "+ username);
        }
        return new UserPrincipal(user);
    }

}
